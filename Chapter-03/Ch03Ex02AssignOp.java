public class Ch03Ex02AssignOp {
    public static void main(String [] args){
        int x, y;
        double a, b;
        char p, q;

        x = y = 100;
        x = 20;
        a = b = 0.012;
        a = 3.14159;
        p = q = '#';
        p = '@';

        System.out.println("x ( " + x + " ), y ( " + y + " )");
        System.out.println("a ( " + a + " ), b ( " + b + " )");
        System.out.println("p ( " + p + " ), q ( " + q + " )");
    }
}
